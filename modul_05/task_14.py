"""ПОШУК НОМЕРА ТЕЛЕФОНУ
Завдання буде схоже на попереднє, але тепер у тексті ми шукатимемо номер телефону України в 
міжнародному форматі, шаблон якого наступний: +380(67)777-7-777 або +380(67)777-77-77"""
import re


def find_all_phones(text: str) -> list:
    """find all phones"""
    result = re.findall(r"\+380\(\d{2}\)\d{3}-\d{2}-\d{2}|\+380\(\d{2}\)\d{3}-\d{1}-\d{3}", text)
    return result


print(find_all_phones("+380(67)777-7-777 або +380(67)777-77-77"))